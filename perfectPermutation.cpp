#include<iostream>
#include<bits/stdc++.h>
using namespace std;
typedef long long ll;
int main()
{
    ll t;
    cin>>t;
    for(ll l=0;l<t;l++)
    {
        ll n;
        cin>>n;
        cout<<n<<" ";
        for(ll i=1;i<=n-1;i++)
        {
            cout<<i<<" ";
        }
        cout<<"\n";
    }
}